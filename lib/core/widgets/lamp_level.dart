/// Состояние огня лампады. Уровни дискретные, а не плавная функция от стрика:
/// плавная честнее, но перемену не заметишь без взгляда на цифру, а весь смысл
/// в том, чтобы её увидели. Выше 7 не дробим — 30/100 дней это уже ачивки.
enum LampLevel {
  /// Стрик 0: лампада как объект видна, но холодная и без свечения.
  unlit,

  /// 1–2 дня: малый огонёк, узкое свечение у самой чаши.
  kindled,

  /// 3–6 дней: полный огонёк, свечение до середины киота.
  steady,

  /// 7+ дней: тёплый свет доходит до арки.
  full;

  /// Отрицательный стрик доменом не запрещён, поэтому попадает в [unlit],
  /// а не роняет виджет.
  static LampLevel forStreak(int streakDays) {
    if (streakDays <= 0) return unlit;
    if (streakDays < 3) return kindled;
    if (streakDays < 7) return steady;
    return full;
  }

  bool get isLit => this != unlit;
}
