import 'package:flutter/foundation.dart';

/// Диагностика сетевого пути: что ушло, что вернулось, сколько заняло.
///
/// Только в debug — в релизной сборке не должно остаться ни строчки. Это
/// инструмент отладки, а не телеметрия: логи сетевых ответов легко превращают
/// консоль в свалку и утекают в продовые сборки, если про них забыть.
void netLog(String message) {
  if (kDebugMode) debugPrint('[net] $message');
}
