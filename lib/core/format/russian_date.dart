/// Дата вида «19 июля» — родительный падеж, как в живой речи.
///
/// Своя реализация вместо `intl`: нужен ровно один формат, а пакет тянет
/// локали и кодогенерацию ради двенадцати слов.
String russianDayMonth(DateTime date) =>
    '${date.day} ${_monthsGenitive[date.month - 1]}';

const _monthsGenitive = [
  'января',
  'февраля',
  'марта',
  'апреля',
  'мая',
  'июня',
  'июля',
  'августа',
  'сентября',
  'октября',
  'ноября',
  'декабря',
];
